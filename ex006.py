# crie um algorítimo que leia um número e mostre o seu dobro, triplo e a sua raiz quadrada.
n = int(input('Digite um número: '))
print('Dobro de {}: {} x 2 = {}.'.format(n, n, n*2))
print('Triplo de {}: {} x 3 = {}.'.format(n, n, n*3))
print('Raiz quadrada de {}: V{} = {:.0f}.'.format(n, n, n**(1/2)))
